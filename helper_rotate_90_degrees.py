# Function to rotate the bitmap 90 degrees clockwise
def rotate_90_degrees(array):
    """
    Rotates a 2D array/matrix 90 degrees clockwise.
    """
    # Get dimensions of the original array
    if not array:
        return []
    
    rows = len(array)
    cols = len(array[0]) if rows > 0 else 0
    
    # Create a new array with swapped dimensions
    rotated = [[0 for _ in range(rows)] for _ in range(cols)]
    
    # Fill the rotated array
    for r in range(rows):
        for c in range(cols):
            # r,c in the original array maps to c,(rows-1-r) in the rotated array
            rotated[c][rows-1-r] = array[r][c]
    
    return rotated

